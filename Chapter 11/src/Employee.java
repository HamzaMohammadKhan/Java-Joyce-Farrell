public class Employee {
    private int empNum;
    private double empSal;

    public int getEmpNum(){
        return empNum;
    }
    public void setEmpNum(int num){
        empNum = num;
    }

    public double getEmpSal(){
        return empSal;
    }
    public void setEmpSal(double sal){
        empSal = sal;
    }
}

public class DemoMath {
    public static void main(String[] args){
        double val = 26.9;
        System.out.println("The value is = " + val);

        System.out.println("Absolute value of val is = ");
        System.out.println(Math.abs(val));

        System.out.println("Absolute value of val is = ");
        System.out.println(Math.abs(-val));

        System.out.println("SQare root value of val is = ");
        System.out.println(Math.sqrt(val));

        System.out.println("round value of val is = ");
        System.out.println(Math.round(val));

        System.out.println("Random value of val is = ");
        System.out.println(Math.random());

        System.out.println("Random value of val is = ");
        System.out.println(Math.pow(8.0, 2));
    }
}
